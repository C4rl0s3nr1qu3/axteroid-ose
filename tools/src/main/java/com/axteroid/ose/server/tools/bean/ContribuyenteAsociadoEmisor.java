package com.axteroid.ose.server.tools.bean;

public class ContribuyenteAsociadoEmisor {

	private Long ruc;
	private Long rucAsociado;
	private Integer tipoAsociacion;
	private String fechaInicio;
	private String fechaFin;
	
	
	public Long getRuc() {
		return ruc;
	}
	public void setRuc(Long ruc) {
		this.ruc = ruc;
	}
	public Long getRucAsociado() {
		return rucAsociado;
	}
	public void setRucAsociado(Long rucAsociado) {
		this.rucAsociado = rucAsociado;
	}
	public Integer getTipoAsociacion() {
		return tipoAsociacion;
	}
	public void setTipoAsociacion(Integer tipoAsociacion) {
		this.tipoAsociacion = tipoAsociacion;
	}
	public String getFechaInicio() {
		return fechaInicio;
	}
	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	public String getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}
	
	
		
	
	
}

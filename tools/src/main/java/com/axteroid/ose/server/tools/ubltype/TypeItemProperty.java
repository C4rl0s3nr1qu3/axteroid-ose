package com.axteroid.ose.server.tools.ubltype;

import java.util.List;

public class TypeItemProperty {
    protected String id;
    protected String name;
    protected String nameCode;
    protected String testMethod;
    protected String value;
    protected String valueQuantity;
    protected List<String> valueQualifier;
    protected String importanceCode;
    protected List<String> listValue;
    protected String usabilityPeriod;
    protected List<String> itemPropertyGroup;
    protected String rangeDimension;
    protected String itemPropertyRange;
    
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNameCode() {
		return nameCode;
	}
	public void setNameCode(String nameCode) {
		this.nameCode = nameCode;
	}
	public String getTestMethod() {
		return testMethod;
	}
	public void setTestMethod(String testMethod) {
		this.testMethod = testMethod;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getValueQuantity() {
		return valueQuantity;
	}
	public void setValueQuantity(String valueQuantity) {
		this.valueQuantity = valueQuantity;
	}
	public List<String> getValueQualifier() {
		return valueQualifier;
	}
	public void setValueQualifier(List<String> valueQualifier) {
		this.valueQualifier = valueQualifier;
	}
	public String getImportanceCode() {
		return importanceCode;
	}
	public void setImportanceCode(String importanceCode) {
		this.importanceCode = importanceCode;
	}
	public List<String> getListValue() {
		return listValue;
	}
	public void setListValue(List<String> listValue) {
		this.listValue = listValue;
	}
	public String getUsabilityPeriod() {
		return usabilityPeriod;
	}
	public void setUsabilityPeriod(String usabilityPeriod) {
		this.usabilityPeriod = usabilityPeriod;
	}
	public List<String> getItemPropertyGroup() {
		return itemPropertyGroup;
	}
	public void setItemPropertyGroup(List<String> itemPropertyGroup) {
		this.itemPropertyGroup = itemPropertyGroup;
	}
	public String getRangeDimension() {
		return rangeDimension;
	}
	public void setRangeDimension(String rangeDimension) {
		this.rangeDimension = rangeDimension;
	}
	public String getItemPropertyRange() {
		return itemPropertyRange;
	}
	public void setItemPropertyRange(String itemPropertyRange) {
		this.itemPropertyRange = itemPropertyRange;
	}
    

    
}

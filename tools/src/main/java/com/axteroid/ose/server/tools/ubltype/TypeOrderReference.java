package com.axteroid.ose.server.tools.ubltype;

public class TypeOrderReference {
	
    protected String id;
    protected String salesOrderID;
    protected String copyIndicator;
    protected String uuid;
    protected String issueDate;
    protected String issueTime;
    protected String customerReference;
    protected String documentReference;
    protected String orderTypeCode;
    
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSalesOrderID() {
		return salesOrderID;
	}
	public void setSalesOrderID(String salesOrderID) {
		this.salesOrderID = salesOrderID;
	}
	public String getCopyIndicator() {
		return copyIndicator;
	}
	public void setCopyIndicator(String copyIndicator) {
		this.copyIndicator = copyIndicator;
	}
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getIssueDate() {
		return issueDate;
	}
	public void setIssueDate(String issueDate) {
		this.issueDate = issueDate;
	}
	public String getIssueTime() {
		return issueTime;
	}
	public void setIssueTime(String issueTime) {
		this.issueTime = issueTime;
	}
	public String getCustomerReference() {
		return customerReference;
	}
	public void setCustomerReference(String customerReference) {
		this.customerReference = customerReference;
	}
	public String getDocumentReference() {
		return documentReference;
	}
	public void setDocumentReference(String documentReference) {
		this.documentReference = documentReference;
	}
	public String getOrderTypeCode() {
		return orderTypeCode;
	}
	public void setOrderTypeCode(String orderTypeCode) {
		this.orderTypeCode = orderTypeCode;
	} 
    
}
